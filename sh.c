// Shell.

#include "types.h"
#include "user.h"
#include "fcntl.h"

// Parsed command representation
#define EXEC  1
#define REDIR 2
#define PIPE  3
#define LIST  4
#define BACK  5

#define MAXARGS 10

struct cmd {
  int type;
};

struct execcmd {
  int type;
  char *argv[MAXARGS];
  char *eargv[MAXARGS];
};

struct redircmd {
  int type;
  struct cmd *cmd;
  char *file;
  char *efile;
  int mode;
  int fd;
};

struct pipecmd {
  int type;
  struct cmd *left;
  struct cmd *right;
};

struct listcmd {
  int type;
  struct cmd *left;
  struct cmd *right;
};

struct backcmd {
  int type;
  struct cmd *cmd;
};

int fork1(void);  // Fork but panics on failure.
void panic(char*);
struct cmd *parsecmd(char*);

// Execute cmd.  Never returns.
void
runcmd(struct cmd *cmd)
{
  int p[2];   //做为pipe调用的参数
  struct backcmd *bcmd;
  struct execcmd *ecmd;
  struct listcmd *lcmd;
  struct pipecmd *pcmd;
  struct redircmd *rcmd;

  if(cmd == 0)
    exit();

  switch(cmd->type){
  default:
    panic("runcmd");

  case EXEC:    //可执行命令
    ecmd = (struct execcmd*)cmd;
    if(ecmd->argv[0] == 0)
      exit();
    exec(ecmd->argv[0], ecmd->argv);
    printf(2, "exec %s failed\n", ecmd->argv[0]);
    break;

  case REDIR:   //重定向命令
    rcmd = (struct redircmd*)cmd;
    close(rcmd->fd);          //关闭标准输入/输出(取决于重定向操作的类型)
    //用指定的模式打开要打开的文件作为新的标准输入/输出
    if(open(rcmd->file, rcmd->mode) < 0){
      printf(2, "open %s failed\n", rcmd->file);
      exit();
    }
    runcmd(rcmd->cmd);
    break;

  case LIST:    //？
    lcmd = (struct listcmd*)cmd;
    if(fork1() == 0)
      runcmd(lcmd->left);
    wait();
    runcmd(lcmd->right);
    break;

  case PIPE:    //管道命令
    pcmd = (struct pipecmd*)cmd;
    if(pipe(p) < 0)
      panic("pipe");
    if(fork1() == 0){
      close(1);   //先关闭标准输出再dup
      dup(p[1]);  //生成一个新的fd指向p[1]，会把标准输出定向到p[1]所指文件，即管道写入端
      //去掉管道对端口的引用
      close(p[0]);
      close(p[1]);
      //左边的标准输入不变，标准输出流入管道写入端
      runcmd(pcmd->left);
    }
    if(fork1() == 0){
      close(0);   //先关闭标准输入再dup
      dup(p[0]);  //生成一个新的fd指向p[0]，会把标准输入定向到 p[0] 所指文件，即管道读取端
      //去掉管道对端口的引用
      close(p[0]);
      close(p[1]);
      //右边的标准输出不变，标准输入从管道读取
      runcmd(pcmd->right);
    }
    close(p[0]);
    close(p[1]);
    wait();
    wait();
    break;

  case BACK:    //？
    bcmd = (struct backcmd*)cmd;
    if(fork1() == 0)
      runcmd(bcmd->cmd);
    break;
  }
  exit();
}

//获取命令
int
getcmd(char *buf, int nbuf)
{
  printf(2, "$ ");
  memset(buf, 0, nbuf);
  gets(buf, nbuf);    //读取nbuf个字符到buf中
  if(buf[0] == 0)     // EOF
    return -1;
  return 0;
}

int
main(void)
{
  static char buf[100];
  int fd;

  // Ensure that three file descriptors are open.
  while((fd = open("console", O_RDWR)) >= 0){
    if(fd >= 3){
      close(fd);
      break;
    }
  }

  //循环调用getcmd获取输入的命令
  // Read and run input commands.
  while(getcmd(buf, sizeof(buf)) >= 0){
    if(buf[0] == 'c' && buf[1] == 'd' && buf[2] == ' '){
      //如果只是cd命令，则切换到指定文件夹后继续等待命令
      //cd命令再父进程完成就行，不需要fork子进程来完成
      // Chdir must be called by the parent, not the child.
      buf[strlen(buf)-1] = 0;  // chop \n  把回车键改成'\0'
      if(chdir(buf+3) < 0)
        printf(2, "cannot cd %s\n", buf+3);
      continue;
    }
    //如果不是cd命令，则fork出子进程运行命令
    if(fork1() == 0)
      runcmd(parsecmd(buf));
    wait();
  }
  exit();
}

//输出s指向的错误信息
void
panic(char *s)
{
  printf(2, "%s\n", s);
  exit();
}

//fork一个子进程，如果失败则将错误信息输出到标准stderr
int
fork1(void)
{
  int pid;

  pid = fork();
  if(pid == -1)
    panic("fork");
  return pid;
}

